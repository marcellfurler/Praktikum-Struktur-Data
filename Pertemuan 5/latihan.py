class Kalkulator:
    def penjumlahan(self, angka1, angka2):
        return(angka1+angka2)
    def pengurangan(self, angka1, angka2):
        return(angka1-angka2)
    def perkalian(self, angka1, angka2):
        return(angka1-angka2)
    def pembagian(self, angka1, angka2):
        return(angka1-angka2)
    